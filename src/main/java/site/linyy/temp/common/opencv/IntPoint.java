package site.linyy.temp.common.opencv;

/**
 * 整数点
 */
public class IntPoint{
	
	int x;
	int y;
	
	public IntPoint(int x,int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
	
}